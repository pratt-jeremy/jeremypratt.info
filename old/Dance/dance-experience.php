<h1>Dance Experience</h1>
                <p>I first began dancing in High School where I attended a community ballroom class
                and took Latin dance. I did well at these dances, though i didn't particularly enjoy them.
                It wasn't until 2011 that I started swing dancing, but it wasn't long after starting
                that I had gained enough skill to teach.</p>
                <h3>Which Dances Do You Teach?</h3>
                <h4>Balboa</h4>
                <p>Blaboa was started in the 1920's.  It's origin story is different
                depending on who you talk to, but the story I like is that people 
                were looking for a dance that could accommodate such little room on the
                dance floor and was derived from Fox Trot.</p>
                <h4>East Coast</h4>
                <p>East Coast is the first swing dance most people learn, and when 
                teaching a beginner, I always start with this, in order to get some
                of the basics of swing dance in them before moving onto more difficult dances</p>
                <h4>Lindy Hop</h4>
                <p>Lindy Hop is what most people think of when they think of swing dance.
                This dance is very linear and uses an 8 count stepping pattern.  
                </p>
                <h4>Charleston</h4>
                <p>Charleston is an upbeat swing dance danced side by side, unless dancing 20's 
                or doing a specific move.</p>
